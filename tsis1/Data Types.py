#Getting the data type
x = 5
print(type(x))

#STR
x = "Hello World"
print(x)
print(type(x)) 

#INT
x = 20
print(x)
print(type(x)) 

#FLOAT
x = 20.5
print(x)
print(type(x)) 

#COMPLEX
x = 1j
print(x)
print(type(x)) 

#LIST
x = ["apple", "banana", "cherry"]
print(x)
print(type(x)) 

#TUPLE
x = ("apple", "banana", "cherry")
print(x)
print(type(x)) 

#RANGE
x = range(6)
print(x)
print(type(x)) 

#DICT
x = {"name" : "John", "age" : 36}
print(x)
print(type(x)) 

#SET
x = {"apple", "banana", "cherry"}
print(x)
print(type(x)) 

#FROZENSET
x = frozenset({"apple", "banana", "cherry"})
print(x)
print(type(x)) 

#BOOL
x = True
print(x)
print(type(x)) 

#BYTES
x = b"Hello"
print(x)
print(type(x)) 

#BYTEARRAY
x = bytearray(5)
print(x)
print(type(x)) 

#MEMORYVIEW
x = memoryview(bytes(5))	
print(x)
print(type(x)) 

#NONE TYPE
x = None	
print(x)
print(type(x)) 